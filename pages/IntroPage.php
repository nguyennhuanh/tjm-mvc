<span style="color:#006"><b>Introduction</b></span>
<p>
If you have developed mobile web applications, I'm sure you know jQuery Mobile.
As for me, a mobile web-app guy, I am very interested in its supported features as stated in Jquery mobile home page:
<blockquote>
"A unified user interface system that works seamlessly across all popular mobile device platforms, 
built on the rock-solid jQuery and jQuery UI foundation. 
Focused on a lightweight codebase built on progressive enhancement with a flexible, easily themeable design."
</blockquote>
</p>
<p>
Development would be more convenient if there was a MVC framework which uses jQuery Mobile in its Views.
With the above thoughts, I have developed this framework and named it Tenkana Jquery Mobile MVC, or TJM-MVC in short.
</p>
<p>
Actually, you can treat this framework like a sandbox project or a starter-kit which helps you build a mobile webapp in few minutes.
By combining the bright architecture of the MVC pattern with the user friendly interface of Jquery Mobile,
TJM-MVC makes building your mobile web application so much more interesting.
In addition, TJM-MVC also provides the features essential for web development such as authentication, caching, SEO, theme-based css and more.
<a href="features" data-role="button">TJM-MVC Features</a>
</p>
<p>
Currently, TJM-MVC is still in alpha phase and is likely to have bugs that need fixing. 
In the future, I would like to add more features and make the framework even easier to use.
So I'm looking forward to your comments and ideas for improvements. Or if you are interested in contributing to the code, 
please get in touch at tenkana@gmail.com or Github.
</p>
